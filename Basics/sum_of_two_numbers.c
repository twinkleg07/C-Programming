#include <stdio.h>
int main()
{
  int a,b;
  
  printf("Enter first number:");
  scanf("%d", &a);
  
  printf("Enter second number:");
  scanf("%d", &b);
  
  printf("Sum of two numbers is: %d", a+b);
  
  return 0;
}
